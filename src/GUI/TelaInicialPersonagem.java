    private void RadioBtnBandidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RadioBtnBandidoActionPerformed
        limparCamposPersonagens();
        personagensSelecionados.clear();
        
        for(int i = 0; i < todosOsPersonagens.size(); i++)
        {
            if(todosOsPersonagens.get(i).getClasse().equals("Bandido"))
            {
                personagensSelecionados.add(todosOsPersonagens.get(i));
            }
        }
        preencherCamposPersonagens(personagensSelecionados);
    }//GEN-LAST:event_RadioBtnBandidoActionPerformed

