    private void preencherCamposPersonagens(ArrayList<Personagem> personagens)
    {
        for(int i = -1; i < personagens.size(); i++) {
            switch (i) {
                case 0 -> CTpersonagem1.setText(personagens.get(i).getNome());
                case 1 -> CTpersonagem2.setText(personagens.get(i).getNome());
                case 2 -> CTpersonagem3.setText(personagens.get(i).getNome());
                case 3 -> CTpersonagem4.setText(personagens.get(i).getNome());
                case 4 -> CTpersonagem5.setText(personagens.get(i).getNome());
                default -> {
                }
            }
        }
    }
    
    public void limparCamposPersonagens()
    {
        CTpersonagem1.setText("");
        CTpersonagem2.setText("");
        CTpersonagem3.setText("");
        CTpersonagem4.setText("");
        CTpersonagem5.setText("");
    }
    
    public void removerPersonagemComCampo(int campo)
    {
        if(personagensSelecionados.size() >= campo)
        {
            personDAO.removerPersonagem(personagensSelecionados.get(campo - 1));
            todosOsPersonagens = userDAO.pesquisarPersonagens(user);
            personagensSelecionados.clear();
            personagensSelecionados.addAll(todosOsPersonagens);
            limparCamposPersonagens();
            preencherCamposPersonagens(todosOsPersonagens);
            RadioBtnTodas.setSelected(true);
        }
    }
    
    public TelaInicialPersonagem(Usuario us) {
        initComponents();
        user = us;
        userDAO = new UsuarioDAO();
        personDAO = new PersonagemDAO();
        todosOsPersonagens = userDAO.pesquisarPersonagens(user);
        personagensSelecionados.addAll(todosOsPersonagens);
        preencherCamposPersonagens(todosOsPersonagens);
        
        LBnome.setText(user.getNome());
        RadioBtnTodas.setSelected(true);
    }

