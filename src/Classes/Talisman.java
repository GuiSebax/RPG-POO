public class Talisman {
    private String nome, proeficiencia, descr;
    private int idTalisman;

    public String getNome() {
        return nome;
    }

    public String getProeficiencia() {
        return proeficiencia;
    }

    public String getDescr() {
        return descr;
    }

    public int getIdTalisman() {
        return idTalisman;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setProeficiencia(String proeficiencia) {
        this.proeficiencia = proeficiencia;
    }

    public void setDescr(String descr) {
        this.descr = descr;
    }

    public void setIdTalisman(int idTalisman) {
        this.idTalisman = idTalisman;
    }
    
    
}
